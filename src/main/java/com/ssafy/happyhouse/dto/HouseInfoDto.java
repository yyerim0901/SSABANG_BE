package com.ssafy.happyhouse.dto;

public class HouseInfoDto {
	
	private int houseno, housebuildyear;
	private String house_aptname, guname, lat, lng, housearea, housefloor, housejibun;
	
	
	
	
	public String getHouse_aptname() {
		return house_aptname;
	}
	public void setHouse_aptname(String house_aptname) {
		this.house_aptname = house_aptname;
	}
	public int getHouseno() {
		return houseno;
	}
	public void setHouseno(int houseno) {
		this.houseno = houseno;
	}
	public int getHousebuildyear() {
		return housebuildyear;
	}
	public void setHousebuildyear(int housebuildyear) {
		this.housebuildyear = housebuildyear;
	}
	public String getGuname() {
		return guname;
	}
	public void setGuname(String guname) {
		this.guname = guname;
	}
	public String getLat() {
		return lat;
	}
	public void setLat(String lat) {
		this.lat = lat;
	}
	public String getLng() {
		return lng;
	}
	public void setLng(String lng) {
		this.lng = lng;
	}
	public String getHousearea() {
		return housearea;
	}
	public void setHousearea(String housearea) {
		this.housearea = housearea;
	}
	public String getHousefloor() {
		return housefloor;
	}
	public void setHousefloor(String housefloor) {
		this.housefloor = housefloor;
	}
	public String getHousejibun() {
		return housejibun;
	}
	public void setHousejibun(String housejibun) {
		this.housejibun = housejibun;
	}
	
	
}
